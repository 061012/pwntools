from pwn import trace, sleep
from pwn.text import color

_banner = '''  .:*~*:._.:*~*:._.:*~*:._.:*~*:._.:*~*:._.:*~*:._.:*~*:._.:*~*:._.:*~*:.
  )     _____                         _    _                            )
  (    |  _  |___ _ _ _ ___ ___ ___ _| |  | |_ _ _                      (
  )    |   __| . | | | | -_|  _| -_| . |  | . | | |                     )
  (    |__|  |___|_____|___|_| |___|___|  |___|_  |                     (
  )          _____                         __ |___|     __              )
  (         /\\  _ `\\                      /\\ \\       __/\\ \\             (
  )         \\ \\ \\Z\\ \\__  __  __   ____    \\ \\ \\     /\\_\\ \\ \\___         )
  (          \\ \\ ,__/\\ \\/\\ \\/\\ \\ /  _ `\\   \\ \\ \\    \\/\\ \\ \\  _ `\\       (
  )           \\ \\ \\/\\ \\ \\_/ \\_/ \\ \\ \\/\\ \\   \\ \\ \\____\\ \\ \\ \\ \\Z\\ \\      )
  (            \\ \\_\\ \\ \\___^___/'\\ \\_\\ \\_\\   \\ \\_____\\\\ \\_\\ \\____/      (
  )             \\/_/  \\/__//__/   \\/_/\\/_/    \\/_____/ \\/_/\\/___/       )
  (                                                                     (
  .:*~*:._.:*~*:._.:*~*:._.:*~*:._.:*~*:._.:*~*:._.:*~*:._.:*~*:._.:*~*:.'''

_lines = _banner.split('\n')

def splash():
    trace('\x1b[G\x1b[?25l')
    for c in range(8):
        for line in _lines:
            trace(color(c % 8, line) + '\n')
            sleep(0.005)
        for _ in _lines:
            trace('\x1b[F')
    for line in _lines:
        trace(line + '\n')
    trace('\x1b[?25h')
